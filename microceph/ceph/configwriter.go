package ceph

import (
	"fmt"
	"os"
	"path/filepath"
	"text/template"
)

// ConfigWriter is an interface for writing config files
type ConfigWriter interface {
	WriteConfig(any) error
}

// Config struct
type Config struct {
	configTemplate *template.Template
	configDir      string
	configFile     string
}

// GetPath returns the path to the config file
func (c *Config) GetPath() string {
	return filepath.Join(c.configDir, c.configFile)
}

func (c *Config) validateConfigFile() error {
	if !filepath.IsLocal(c.configFile) || filepath.Base(c.configFile) != c.configFile {
		return fmt.Errorf("invalid config filename %q", c.configFile)
	}

	return nil
}

// WriteConfig writes the configuration file given a data bag and a filemode.
// It writes atomically: the template is rendered to a uniquely-named temporary
// file (created via os.CreateTemp in the same directory as the destination) and
// then renamed into place. Using a unique temp file name — rather than a fixed
// ".tmp" path — means concurrent renders of the same config file cannot collide
// or rename a partially-written file from another writer into place, so the
// atomic-rename guarantee holds under concurrency (AGENTS.md "Atomic file
// writes"). A failed write cannot leave partial state on disk: the temp file is
// removed on every error path.
func (c *Config) WriteConfig(data map[string]any, mode int) error {
	err := c.validateConfigFile()
	if err != nil {
		return err
	}

	destPath := c.GetPath()

	// os.CreateTemp creates the file with O_RDWR|O_CREATE|O_EXCL and mode 0600,
	// so two concurrent writers can never pick the same name. Create it in the
	// same directory as the destination so os.Rename is same-filesystem/atomic.
	fd, err := os.CreateTemp(filepath.Dir(destPath), filepath.Base(destPath)+".*")
	if err != nil {
		return fmt.Errorf("Couldn't write %s: %w", c.configFile, err)
	}
	tmpPath := fd.Name()

	// os.CreateTemp ignores the requested mode (uses 0600), so apply it now so
	// the final renamed file has the requested permissions.
	err = os.Chmod(tmpPath, os.FileMode(mode))
	if err != nil {
		fd.Close()
		os.Remove(tmpPath)
		return fmt.Errorf("Couldn't write %s: %w", c.configFile, err)
	}

	err = c.configTemplate.Execute(fd, data)
	closeErr := fd.Close()
	if err != nil {
		os.Remove(tmpPath)
		return fmt.Errorf("Couldn't render %s: %w", c.configFile, err)
	}
	if closeErr != nil {
		os.Remove(tmpPath)
		return fmt.Errorf("Couldn't close %s: %w", c.configFile, closeErr)
	}

	err = os.Rename(tmpPath, destPath)
	if err != nil {
		os.Remove(tmpPath)
		return fmt.Errorf("Couldn't rename %s: %w", c.configFile, err)
	}

	return nil
}

// newRadosGWConfig creates a new radosgw config file
func newRadosGWConfig(configDir string) *Config {
	return &Config{
		configTemplate: template.Must(template.New("radosgwConfig").Parse(`# Generated by MicroCeph, DO NOT EDIT.
[global]
mon host = {{.monitors}}
run dir = {{.runDir}}
auth allow insecure global id reclaim = false

[client.radosgw.gateway]
rgw init timeout = 1200
rgw frontends = beast{{if or (ne .rgwPort 0) (not .sslCertificatePath) (not .sslPrivateKeyPath)}} port={{.rgwPort}}{{end}}{{if and .sslCertificatePath .sslPrivateKeyPath}} ssl_port={{.sslPort}} ssl_certificate={{.sslCertificatePath}} ssl_private_key={{.sslPrivateKeyPath}}{{end}}
`)),
		configFile: "radosgw.conf",
		configDir:  configDir,
	}
}

// newGaneshaConfig creates a new NFS Ganesha config file
func newGaneshaConfig(configDir string) *Config {
	return &Config{
		configTemplate: template.Must(template.New("ganeshaConfig").Parse(`# Generated by MicroCeph, DO NOT EDIT.

NFS_CORE_PARAM {
	Enable_NLM = false;
	Enable_RQUOTA = false;
	Protocols = 4;
	Bind_Addr = {{.bindAddr}};
	NFS_Port = {{.bindPort}};
	Plugins_Dir = "{{.snapDir}}/lib/x86_64-linux-gnu/ganesha";
}

NFSv4 {
	Delegations = false;
	RecoveryBackend = 'rados_cluster';
	Minor_Versions = {{.minorVersions}};
	UseGetpwnam = true;
}

# The libcephfs client will aggressively cache information while it
# can, so there is little benefit to ganesha actively caching the same
# objects. Doing so can also hurt cache coherency. Here, we disable
# as much attribute and directory caching as we can.
MDCACHE {
        # Size the dirent cache down as small as possible.
        Dir_Chunk = 0;
}

NFS_KRB5 {
	CCacheDir = "{{.runDir}}/ganesha";
}

RADOS_KV {
	ceph_conf = "{{.confDir}}/ceph.conf";
	UserId = "{{.userID}}";
	pool = ".nfs";
	namespace = "{{.clusterID}}";
}

RADOS_URLS {
	ceph_conf = "{{.confDir}}/ceph.conf";
	UserId = "{{.userID}}";
	watch_url = "rados://.nfs/{{.clusterID}}/conf-nfs.{{.clusterID}}";
}

LOG {
	Components {
		ALL = EVENT;
	}
}

%url    rados://.nfs/{{.clusterID}}/conf-nfs.{{.clusterID}}
`)),
		configFile: "ganesha.conf",
		configDir:  configDir,
	}
}

